/**
 * @param {number[][]} obstacleGrid
 * @return {number}
 */
var uniquePathsWithObstacles = function (obstacleGrid) {
  const M = obstacleGrid.length;
  const N = obstacleGrid[0].length;
  const memo = new Array(M).fill(0).map((v) => new Array(N).fill(0));

  const explorePaths = (x, y) => {
    if (obstacleGrid[x] === undefined || obstacleGrid[x][y] === undefined || obstacleGrid[x][y] === 1) return 0;

    // at this Point we have Reached the Target
    if (x === M - 1 && y === N - 1) {
      return 1;
    }

    if (memo[x][y] === 0) {
      memo[x][y] = explorePaths(x + 1, y) + explorePaths(x, y + 1);
    }
    return memo[x][y];
  };

  return explorePaths(0, 0);
};

